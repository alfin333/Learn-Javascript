//  = assignment operator
//  == comparison operator
//  === strict equality operator

let x = 3.14; // ini assignment operator karena -

// yang bawah ini comparison operator karena ==
if(x == 3.14){
    console.log(`That is Pi!`);
}
else{
    console.log(`That is NOT Pi!`);
}

let z = "3.14";

// ini untuk membandingkan nilai sekaligus tipe datanya ===
if(z === 3.14){
    console.log(`That is Pi!`);
}
else{
    console.log(`That is NOT Pi!`);
}